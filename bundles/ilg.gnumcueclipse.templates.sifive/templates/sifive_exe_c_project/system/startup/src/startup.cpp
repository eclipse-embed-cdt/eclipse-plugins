/**
 * @details
 * This function resets the MCU core, if possible.
 */
void
__attribute__ ((noreturn,weak))
os_terminate (int code __attribute__((unused)))
{
#if defined(__ARM_EABI__)
  NVIC_SystemReset ();
#elif defined(__riscv)
  // TODO: find a RISC-V soft reset
#endif
  while (true)
    {
      ;
    }
  /* NOTREACHED */
}

